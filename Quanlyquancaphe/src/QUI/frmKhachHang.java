    private void tbl_khMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khMouseClicked
        // TODO add your handling code here:
         try {
        int row = tbl_kh.getSelectedRow();
        if (row >= 0) {
            String id = String.valueOf(tbl_kh.getValueAt(row, 0));
            KhachHangDAO dao = new KhachHangDAO();
            KhachHang nv = dao.findById(id);
            if (nv != null) {
                txtMaKH.setText(String.valueOf(nv.getId()));
                txtTenKH.setText(nv.getTen_kh());     
                txtSdt.setText(nv.getSodt_kh());
                
                // Handle date conversion for Ngaybatdaulamviec
                if (nv.getNgaysinh_kh()!= null) {
                    txtNgaySinhKH.setDate(nv.getNgaysinh_kh());
                } else {
                    txtNgaySinhKH.setDate(null);
                }
                
                // Set selected item for cboLuongnv;
                txtDiaChiKH.setText(nv.getDiachi_kh());
            }
        }
    } catch (Exception ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_tbl_khMouseClicked

