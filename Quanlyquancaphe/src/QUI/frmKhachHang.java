    public void fillDataToTable(){
        try {
//Lầy Model của bảng và xóa sạch các hàng
        tblModel = (DefaultTableModel) tbl_kh.getModel(); 
        tblModel.setRowCount(0);
        //Lấy dữ liệu từ bảng Students đồ vào list
        KhachHangDAO dao = new  KhachHangDAO();
        List<MODEL.KhachHang> list = dao.readAllKhachHang();
        //Duyệt list và bổ sung các sv vào bảng
        for (KhachHang it : list) {
        Object[] newRow = new Object[]{ 
            it.getId(), it.getTen_kh(), it.getSodt_kh(), it.getNgaysinh_kh(),
            it.getDiachi_kh()};
            tblModel.addRow(newRow);
        }

        } catch (Exception ex) {
            ex.printStackTrace();
    }
    }
