    private void loadData() {
        ChiTietHDDAO dao = new ChiTietHDDAO();
    List<Chitiethoadon> list = dao.getAllChiTietHD();
    DefaultTableModel model = (DefaultTableModel) tblchitiethd.getModel();
    model.setRowCount(0); // Clear existing rows

    for (Chitiethoadon ct : list) {
        Object[] row = new Object[5];
        row[0] = ct.getProductName();
        row[1] = ct.getQuantity();
        row[2] = ct.getCustomerId();
        row[3] = ct.getPaymentDate();
        row[4] = ct.getTotalAmount();
        model.addRow(row);
    }

    }
    private void displaySearchResult(List<Chitiethoadon> resultList) {
    DefaultTableModel model = (DefaultTableModel) tblchitiethd.getModel();
    model.setRowCount(0); // Xóa tất cả các dòng hiện có

    for (Chitiethoadon ct : resultList) {
        Object[] row = new Object[5];
        row[0] = ct.getProductName();
        row[1] = ct.getQuantity();
        row[2] = ct.getCustomerId();
        row[3] = ct.getPaymentDate();
        row[4] = ct.getTotalAmount();
        model.addRow(row);
    }
}
private void refreshForm() {
    // Xóa dữ liệu hiện tại trên bảng (nếu cần)
    DefaultTableModel model = (DefaultTableModel) tblchitiethd.getModel();
    model.setRowCount(0); // Xóa toàn bộ dòng trong bảng
    
    // Đặt lại các trường nhập liệu về trạng thái ban đầu
    txtTenSP.setText("");
    txt_ngaylap.setDate(null);
}
