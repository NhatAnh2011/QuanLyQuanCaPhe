    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
    String tenSP = txtTenSP.getText();
    Date ngayLap = txt_ngaylap.getDate();
    
    // Kiểm tra nếu cả hai trường đều trống thì thông báo lỗi
    if (tenSP.isEmpty() && ngayLap == null) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin tìm kiếm.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Tạo một đối tượng DAO để thực hiện tìm kiếm
    ChiTietHDDAO dao = new ChiTietHDDAO();
    
    // Thực hiện tìm kiếm dựa trên thông tin nhập vào
    List<Chitiethoadon> searchResult;
    if (!tenSP.isEmpty() && ngayLap != null) {
        searchResult = dao.searchByProductNameAndDate(tenSP, ngayLap);
    } else if (!tenSP.isEmpty()) {
        searchResult = dao.searchByProductName(tenSP);
    } else {
        searchResult = dao.searchByDate(ngayLap);
    }
    
    // Hiển thị kết quả tìm kiếm trên bảng
    displaySearchResult(searchResult);
    }//GEN-LAST:event_btnTimKiemActionPerformed

